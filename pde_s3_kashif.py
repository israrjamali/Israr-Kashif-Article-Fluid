strip_h = 0.0            # top & bottom strips (optional)
vert_w  = 0.0            # left & right strips (optional)

target_points = 9000     # desired interior points

# -------------------------
# Search for best n_points (staggered grid)
# -------------------------
n_points = 150
best_pts, best_n = 0, n_points

while n_points > 10:

    xmin, xmax = vert_w, Lx - vert_w
    ymin, ymax = strip_h, Ly - strip_h

    x_vals = torch.linspace(xmin, xmax, n_points, device=device)
    y_vals = torch.linspace(ymin, ymax, n_points, device=device)

